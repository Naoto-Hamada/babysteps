Rails.application.routes.draw do
  devise_for :users
  resources :articles, only: [:new, :create]
  resources :tweets,   only: [:new, :create]
  get 'tweets' => 'tweets#index'
  root 'articles#index'
  get 'info' => 'top#info'
  get 'profile' => 'top#profile'
end


